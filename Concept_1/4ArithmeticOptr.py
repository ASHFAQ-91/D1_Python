# Types of Operator:-
# 1. Arithmetic Optr -> add, subtract, multi, division, floar division, modules.

print(5+3)      # add.
print(10-4)     # subtract.
print(6*7)      # multiplication.
print(17/3)     # Division.
print(17//3)    # Floor Division (Round down to integer).
print(17%3)     # Modules (reminder).
print(2**3)     # Exponent (we can say "Square").